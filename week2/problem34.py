# 모스 부호 

def solution(letter):
    morse_code = { 
    '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
    '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
    '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
    '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
    '-.--':'y','--..':'z'
}
    return ''.join (morse_code[i] for i in letter.split(' '))

# letter 의 모스부호는 공백으로 나누어져 있음
# 공백으로 split 하여 나누어주고, 나누어준 배열을 dict에서 찾아서 해당값 추출